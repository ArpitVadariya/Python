# This is Logical Operator Program

var1 = 30
var2 = 20

#  and or not

print("value of var1 is " + str(var1))
print("value of var2 is " + str(var2))

print("Returns True if both statements are true --> " + str(var1<50 and var2>30))


print("Returns True if one statements is true --> " + str(var1<50 or var2>30))


print("Returns reverse of result --> " + str(not(var1<50 and var2>30)))