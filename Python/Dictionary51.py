# This is Dictionary Program

print("\n in dictionary we add pair key value seprated by : into curly bracket")
print("\n example :- ")

print("\n {1 : 'Arpit', 2 : 'Rocky', 3 : 'Garuda', 4 : 'Vikram', 5 : 'Dilli', 6 : 'Rolex'}")
print("\n here 1, 2, 3, 4, 5, 6 are keys")
print("\n here Arpit Rocky Garuda Vikram Dilli Rolex are values")

Dict1 = {1 : 'Arpit', 2 : 'Rocky', 3 : 'Garuda', 4 : 'Vikram', 5 : 'Dilli', 6 : 'Rolex'}
print(Dict1)

print("\n to access any value of dictionary use Dict1[1]")
print(Dict1[1])

print("\n to change any value use Dict1[3] = 'Adheera' ")
Dict1[3] = "Adheera"
print(Dict1)